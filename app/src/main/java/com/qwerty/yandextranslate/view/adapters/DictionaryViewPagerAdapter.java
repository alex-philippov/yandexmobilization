package com.qwerty.yandextranslate.view.adapters;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

public class DictionaryViewPagerAdapter extends FragmentPagerAdapter {
    private Fragment mFragments[];
    private String mTitles[];

    public DictionaryViewPagerAdapter(FragmentManager fm, Fragment fragments[], String[] titles) {
        super(fm);
        mFragments = fragments;
        mTitles = titles;
    }

    @Override
    public Fragment getItem(int position) {
        return mFragments[position];
    }

    @Override
    public int getCount() {
        return mFragments.length;
    }

    @Override
    public CharSequence getPageTitle(int position) {
        return mTitles[position];
    }
}
